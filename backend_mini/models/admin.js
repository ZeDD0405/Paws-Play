const { Schema, model } = require('mongoose');

const adminSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  isActive: {
    type: Boolean,
    default: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

// Create the Admin model with the schema
const Admin = model('Admin', adminSchema);

module.exports = Admin; 